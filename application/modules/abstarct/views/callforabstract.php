      <h4 class="text-center" style="text-transform:uppercase;font-size:23px;">The following information is provided to guide you through the abstract submission
      process:</h4><br>
      <h3>Submission Guidelines Overview</h3>
      <p>All abstracts for the ISPSM-AGOI conference must be submitted by email to <a href="mailto:abstracts.ispsmagoi@gmail.com">abstracts.ispsmagoi@gmail.com</a></p>

      <h4>The topic of the abstracts should cover the following:</h4>
      <ul>
        <li>Peritoneal surface malignancies</li>
        <li>Gynecological Malignancies</li>
        <li>Gastrointestinal Malignancies</li>
        <li>Pseudomyxoma peritonei (PMP)</li>
        <li>HIPEC</li>
        <li>PIPEC</li>
      </ul>

      <h3>Word limit - 250 words</h3>
      <p>All abstracts must be submitted by email no later than 12 April 2019. Abstracts will be selected for either Oral or Poster presentation. Authors will receive a confirmation email on or before 15 April 2019.</p>

      <h3>Presentation Types:</h3>
      <p>Authors may submit 1 or more abstracts for consideration for presentation at the meeting. All abstracts will be reviewed by the selection committee. The selected abstracts will be shortlisted for either Oral or Poster presentation.<br>Oral presentation will be delivered during the scientific sessions. Date & time of the presentation will be intimated. Each presenter will be given 7 minutes for presentation, followed by 3 minutes for questions. Presenting author must be identified in the submitted abstracts.</p>

      <h3>Your Abstract should adhere to the following format:</h3>
      <ul>
        <li><span>Title : </span>Your title should be as concise and accurate as possible. Good titles may ask aquestion, describe a problem, reference a controversy, summarize an experience, or even state a result.</li>
        <li><span>Aim : </span>State in 1-2 sentences the aim of your study. Describe the scientific context, i.e. why you did the study and include what the hypothesis of the study was. Novelty for its own sake without a clear justification is insufficient reason for a study.</li>
        <li><span>Methods :</span> Briefly state the plan and/or methods used, i.e. how you did the study. For reports of clinical experience it is essential to describe the patient population and how they were identified and / or selected. For comparisons between two populations, it is particularly important to show how the two groups are comparable and / or different.</li>
        <li><span>Results :</span> Clearly summarize the data, i.e. what you found. Use that format which most concisely and clearly presents your results. Abstracts must be complete and include data in the results. Abstracts that do not include results (“results will be presented”) will be automatically rejected.</li>
        <li><span>Conclusions : </span>Briefly state the implications of your findings, i.e. what the results mean.</li>
      </ul>
